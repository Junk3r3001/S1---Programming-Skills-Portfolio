#include <iostream>
int main() {
    // Step 1: Declare an integer variable and initialize with 8
    int num1 = 8;

    // Step 2: Declare another integer variable and initialize with 10
    int num2 = 10;

    // Step 3: Declare a third integer variable that adds the previous two
    int sum = num1 + num2;

    // Step 4: Print the value of the sum to the console
    std::cout << "The sum is: " << sum << std::endl;

    return 0;
}
